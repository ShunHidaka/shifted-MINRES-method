import numpy as np
import scipy as sp

N     = 10
fname = "sample-matrix.mtx"
cmnt  = "Generated by matrix-generator.py"

Apre = np.random.randint(-9, 10, (N, N))
Apst = (Apre + Apre.T) / 2.0

sp.io.mmwrite(fname, sp.sparse.coo_matrix(Apst), comment=cmnt, field='real', precision=5, symmetry='symmetric')
